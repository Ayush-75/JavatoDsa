package basic_1;

public class DataTypes { // primitive data type

    public static void main(String[] args) {
        // byte default value = 0, 1byte/8bits, range -128 to 127
        byte age = 127;
        //short default value = 0, 2byte/16bits
        short number = 32767;
        //int 4byte/32bits
        int mediumNumber = 584554;
        //long 8byte/64bits
        long bigNumber = 5484565;
        //float 4byte/32bits
        float decimalNumber = 5.1f;
        //Double 8byte/64bits
        Double bigDecimalNumber = 5421.154444d;
        //char 2byte/16bits
        char character = 'A';
        //Boolean 1bit,default false
        boolean isFalse = true;

    }
}
